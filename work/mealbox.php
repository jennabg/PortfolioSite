  Ve’ahavta’s Meal Box Program addresses the connection between food
  insecurity, poverty, economic vulnerability and social isolation. The program provides
  work placements for individuals with lived experience of
  poverty/homelessness and trains them to work in a kitchen and
  prepare healthy meals. The meals are then delivered to individuals living on the
  streets, providing them with a variety of tasty and nutritious meal
  options.<br />

For the launch of Ve’ahavta’s Meal Box program I was tasked with creating
an identity for the new program. The style had to match Ve’ahavta’s
branding, but still be distinguishable and easy to understand at a glance.
The icons are all hand drawn and assembled in a way that conveys the core
concept of the Meal Box Program. The icons and branding have been
repurposed in multiple different formats to continue to promote and raise
awareness for the program.<br />

